#include "libft.h"

char		*ft_tolower_str(char *str)
{
	size_t	i;

	i = 0;
	while (*(str + i))
	{
		(*(str + i) >= 65 && *(str + i) <= 90) ? *(str + i) += 32 : *(str + i);
		i++;
	}
	return (str);
}
